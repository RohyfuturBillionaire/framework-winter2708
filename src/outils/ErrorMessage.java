package outils;

public class ErrorMessage {
   String message;
   String value;
   
   public void setMessage(String message) {
       this.message = message;
   }

   public void setValue(String value) {
       this.value = value;
   }

   public String getMessage() {
       return message;
   }

   public String getValue() {
       return value;
   }
   
}
